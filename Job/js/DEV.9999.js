var Auth = {
    changeAuth: function (param) {
        var args = {
            request: "PAGE",
            url: "../Service/svc_Auth.aspx" +
                    "?QUERY=PLM_AUTH_ID" +
                    "&arg_login_id=" + param.login_id,
            block: true,
            handler_success: successRequest
        };
        gw_com_module.callRequest(args);
        function successRequest(data) {
            parent.location.reload();
        }
    }
}

